class Map
{
public:
	/**
	 * \brief Create a random map 
	 */
	Map();
	/**
	 * \brief Create a map based on a existing map 
	 * \param tiles short representation of the map
	 */
	Map(std::vector<std::vector<short>> tiles);
	~Map();

	/**
	 * \brief Print the map to the screen
	 */
	void Print();

	/**
	 * \brief Get a random position and remove it from free position available
	 * \return a random position if one is available. If there is no free position left, return wrong_position 
	 */
	Position GetRandomPosition();
	/**
	 * \brief Check if a given position is free
	 * \param pos
	 * \return true if free
	 */
	bool IsFree(Position pos) const;
	/**
	 * \brief Check if a given position is free
	 * \param x coord x
	 * \param y coord y
	 * \return true if free
	 */
	bool IsFree(int x, int y) const;
	/**
	 * \brief Check if a given tile is free
	 * \param tile
	 * \return true if free
	 */
	bool IsFree(Tile tile) const;
	/**
	 * \brief Add player to the map
	 * \param player 
	 */
	void AddPlayer(Player& player);
	/**
	 * \brief Add a potion into the map
	 * \param potion 
	 */
	void AddPotion(Potion& potion);
	/**
	 * \brief Remove a potion from the map
	 * \param potion 
	 */
	void RemovePotion(Potion& potion);
	/**
	 * \brief Add a potion in the map
	 * \param enemy 
	 */
	void AddEnemy(Enemy& enemy);
	/**
	 * \brief Remove an enemy from the map
	 * \param enemy 
	 */
	void RemoveEnemy(Enemy& enemy);

	void Update();

private:
	static std::vector<std::string> TileToChar(Tile tile);

	Position Index2Pos(int index) const;
	int Pos2Index(Position pos) const;
	int Pos2Index(int x, int y) const;
		
	int sizeX_ = 50;
	int sizeY_ = 50;

	std::vector<Tile> tiles_ = std::vector<Tile>(sizeY_ * sizeX_);
	std::vector<int> freeTiles_;

	Player* player_;

	std::vector<Potion*> potions_;
	std::vector<Enemy*> enemies_;
};
