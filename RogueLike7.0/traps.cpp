#include <iostream>
#include "traps.h"
#include "player.h"

void MapTraps()
{
	char map_trap = 'T';
	Player player;

	if (Player::MoveSet = 'T')
	{
		player.playerHealth = player.playerHealth - trapsDamage;
		std::cout << "You walked on a trap! \n" << map_trap << " You lost 5 Health Points. \n";
	}

}

